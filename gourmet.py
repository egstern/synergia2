    def generate_actions(self):
        self.delete_actions()
        self.iterator.reset()
        element = self.iterator.next()
        s = 0.0
        particle = self.get_initial_particle()
        jet_particle = self.get_initial_jet_particle()
        has_propagated = 0
        energy = self.initial_energy
        while element:
            split_name = element.Name().split(":")
            if split_name[0] == "synergia action":
                if has_propagated:
                    mapping = mappers.Fast_mapping(self.get_u(energy),
                                                   jet_particle.State())
                    new_energy = jet_particle.ReferenceEnergy()
                    self.actions.append(
                        Action("mapping",
                               length=s,
                               initial_energy=energy,
                               final_energy=new_energy,
                               data=mapping))
                    energy = new_energy
                    s = element.OrbitLength(particle)
                    element.propagateParticle(particle)
                    element.propagateJetParticle(jet_particle)
                new_energy = jet_particle.ReferenceEnergy()
                self.actions.append(
                    Action("synergia action",
                           length=s,
                           initial_energy=energy,
                           final_energy=new_energy,
                           data=element,
                           synergia_action=string.join(split_name[1:],':')))
                energy = new_energy           
                jet_particle = self.get_jet_particle(energy)
                has_propagated = 0
                s = 0.0
            else:
                if not element.Type() == "marker":
                    s += element.OrbitLength(particle)
                    element.propagateParticle(particle)
                    element.propagateJetParticle(jet_particle)
                    has_propagated = 1
            element = self.iterator.next()
        self.final_energy = jet_particle.ReferenceEnergy()
        self.have_actions = 1

    def get_actions(self):
        if not self.have_actions:
            self.generate_actions()
        return self.actions

